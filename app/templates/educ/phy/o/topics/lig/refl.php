	    <h4>Procedure</h4>
	    <ul id="listAlpha">
		<li><p>Fix a white sheet of paper on a soft board using drawing pins.</p></li>
		<li><p>Draw a horizontal line and draw a normal QN to it.</p></li>
		<li><p>
			Measure angle of Incidence, <i style="font-family:Times New Roman;">i</i> to the normal
			and draw the line to meet the normal at N.
		    </p></li>
		<li><p>Place a plane mirror vertically along the horizontal line.</p></li>
		<li><p>
			Fix two pins P<sub>1</sub> and P<sub>2</sub> vertically along the Line AN and locate the
			Images using pins P<sub>3</sub> and P<sub>4</sub> so that they appear to be in line with the
			images of P<sub>1</sub> and P<sub>2</sub> as shown in the diagram.
		    </p></li>

		<li><p>Remove the pins and draw a line NB through P<sub>3</sub> and P<sub>4</sub> and measure the angle
			<i style="font-family: Times New Roman;">r</i>.
		    </p></li>
		<li><p>Repeat procedure (c) to (f) for different values of <i style="font-family: Times New Roman;">i</i>.</p></li>
		<li><p>It is observed that <i style="font-family: Times New Roman;">i</i> = <i style="font-family: Times New Roman;">r</i>
			in each case and all the rays line in the same plane with the normal at the point of incidence
			verifying <a href="#laws_of_reflection">the laws of reflection of Light</a>.
