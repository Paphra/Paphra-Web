	    <div class="columns">
		<h4 id="regular_reflection">Regular (Specular) reflection</h4>
		<p>
		    This is a type of reflection where a parallel beam of light is incident on a smooth surface and it is
		    reflected as a parallel beam.
		</p>
	    </div>
