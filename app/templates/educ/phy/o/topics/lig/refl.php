		<li><p>Concave (Converging) Mirror</p></li>
		<li><p>Convex (Diverging) Mirror</p></li>
	    </ul>
	    <p>A convex mirror is formed when the outer surface of the sphere is silvered while a concave
		mirror is formed when the inner surface of the sphere is silvered. </p>

