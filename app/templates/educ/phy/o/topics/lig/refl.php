	</div>
	<div class="six columns">
	    <ul id="listNum">
		<li><h4>Incident ray, AO</h4>
		    <p>This is the ray of light from the light source falling onto the reflecting surface.</p></li>
		<li><h4>reflected ray, OB</h4>
		    <p>This is the ray of light which bounces off from the reflecting surface.</p></li>
		<li><h4>Normal, ON</h4>
		    <p>This is an imaginary line at a right to the reflecting surface.</p></li>
		<li><h4>Angle of Incidence, <i style="font-family: Times New Roman;">i</i></h4>
		    <p>This is the angle between the incident ray and the normal.</p></li>
		<li><h4>Angle of Reflection, <i style="font-family: Times New Roman;">r</i></h4>
		    <p>This is the angle between the reflected ray and the Normal.</p></li>
		<li><h4>Glancing angle, <i style="font-family: Times New Roman;">g</i></h4>
		    <p>This is the angle between the indent ray, or, the reflected ray, and the reflecting surface.</p></li>
	    </ul>
	</div>
    </div>
    <hr/>
    <div class="row">
	<div class="twelve columns">
	    <h3 id="laws_of_reflection">Laws Of Reflection</h3>
	    <ul id="listNum">
		<li><h4>First Law</h4>
		    <p>The incident ray, reflected ray and the Normal, all lie in the same plane.</p></li>
		<li><h4>Second Law</h4>
		    <p>The angle of Incidence is always equal to the angle of Reflection</p></li>
	    </ul>
	</div>
