	    <h3>Terms Used</h3>
	    <p>Consider a ray of light AO incident on a smooth reflecting surface and is reflected along OB as shown
		in the diagram.</p>
	</div>

	<div class="six columns">
	    <img onclick="showImage('/phy/olevel/topics/lig/images/reflection_terms.png')"
