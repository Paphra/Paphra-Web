	    <h3 id="termreflection_">Reflection Of Light</h3>
	    <p>Reflection is a process by which <a>light</a> energy bounces off from the reflecting surface.
		The reflecting surface can be smooth or rough.
		The way light is reflected depends on the nature of the surface. There are two types of reflection of
		light these are;</p>
	    <ul id="listRm">
		<li><p>Regular (Specular) Reflection</p></li>
		<li><p>Irregular (Diffuse) reflection</p></li>
	    </ul>

	</div>
	<br/>
    </div>
