	    <h3 id="image_in_plane_mirror">Image Formed by Plane Mirrors</h3>
	    <p>
		Light rays from an object incident on a plane mirror are reflected according to the laws of reflection
		To an observer, the reflected rays appear to come from the Image <i style="font-family: Times New Roman;">I</i> which is at the same distance behind the mirror
		as the object <i style="font-family: Times New Roman;">O</i> is in front of the mirror.
	    </p>
	    <h4>Characteristics of Images Formed by Plane Mirrors</h4>
	    <ul id="listSq">
		<li><p>The image s virtual (Behind the Mirror)</p></li>
		<li><p>The Image is Laterally inverted (Left is on the right and the reverse is true)</p></li>
		<li><p>The Image is the save size as the object.</p></li>
		<li><p>The Image is the same distance behind the mirror as the object is in front of the Mirror</p></li>
		<li><p>The image is the upright (Erect)</p></li>
	    </ul>

