	<div class="twelve columns">
	    <h4 id="terms_used">Terms Used </h4>
	    <ul id="listNum">
		<li><h5 id="center_of_curvature">Center of Curvature, C</h5>
		    <p>This is the center of the sphere of which the mirror forms a part.</p>
		</li>
		<li><h5 id="aperture">Aperture, AB</h5>
		    <p>This it the width of the mirror.</p>
		</li>
		<li><h5 id="pole">Pole, P</h5>
		    <p>This is the center point of the mirror.</p>
		</li>
		<li><h5 id="principal_axis">Principal Axis</h5>
		    <p>This is the line that joins the pole to the center of curvature of the mirror.</p>
		</li>
		<li><h5 id="focal_length">Focal Length, f</h5>
		    <p>This the distance between the principal focus and the pole of the mirror.</p>
		</li>
		<li><h5 id="radius_of_curvature">Radius Of Curvature, <i style="font-family:Times New Roman;">r</i></h5>
		    <p>This is the distance between the center of curvature and the pole of the mirror. <b>OR</b>
			This is the radius of the sphere of which the mirror forms a part.
		    </p>
		    <p><b>Note:</b> The radius of curvature is twice the focal length of the mirror, <i style="font-family:Times New Roman;">
			    r = 2f</i>
		    </p>
		</li>
		<li><h5 id="principal_focus_of_convex">Principal Focus of a convex mirror, F</h5>
		    <div class="row">
			<div class="eight columns">
			    <p>It is a point on the principal axis to which all rays parallel and close to the principal axis appear to
				diverge after reflection.
			    </p>
