		    <h3 id="slope">8. Slope.</h3>
		    <br/>
		    <div>
			<p>
			    When obtaining the slope (Gradient) of the graph, use as much of the graph paper as possible.
			    If the graph is a straight line graph, draw a large right angled triangle covering all the plotted points.

			    The triangle drawn must touch the line of best fit and the two points on the line of best fit selected for calculating the slope
			    must enclose all the plotted points where possible.
			</p>
			<h4 id="calculating_the_slope">Calculating the slope:</h4>
			<div id="">
			    <b>Method 1:</b>
			    <div>
				<nums>
				    <ul id="">
					<li>Read the coordinates of the two selected points.</li>
					<li>Obtain the slope from the expression;</li><br/>
					<li>Slope = <sup>( Change in the Vertical Axis )</sup>/<sub>( Change in the Horizontal Axis )</sub> .</li>
				    </ul>
				</nums>
			    </div>

			    <b>Method 2:</b><br/>
			    <div>
				<nums>
				    <ul id="">
					<li>Count the number of small squares on each line from the conner of the triangle to the point selected along both the vertical and
					    horizontal axes.</li>
					<li>The change in the Vertical axis is obtained by multiplying the number of the small squares counted by what each small square
					    represents on the Vertical axis.</li>
					<li>The change in the Horizontal axis is obtained by multiplying the number of small squares counted by what each small square represents
					    on the Horizontal axis.</li>
					<li>Then, the slope is determined from the expression below;</li>

					<li>Slope = <sup>( Change in the Vertical Axis )</sup>/<sub>( Change in the Horizontal Axis )</sub> .</li>
				    </ul>
				</nums>
			    </div>
			</div>

			<p>
			    <b>NOTE:</b>
			    When calculating the slope, the working should be clearly shown.
			    The slope must be correctly calculated to the correct number of significant figures and have
			    correct units attached. The unit of the slope must be derived from the units of the axes of the graph.
			</p>
		    </div>
		    <hr/>
		    <h3 id="units_of_the_slope">9. Units of the Slope.</h3>
		    <br/>
		    <div>
			<p>
			    The units of the slope must be derived from the labels on the axes of the graph.
			    If the value of the slope is not in SI Units, convert to SI Units before using it in the next stage except for light
			    experiments.
			</p>
		    </div>
		</div>
