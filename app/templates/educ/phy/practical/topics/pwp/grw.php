		    <h3 id="intercept">3. Intercepts</h3>
		    <div>
			<p>
			    The intercept on any axis is the value of the quantity plotted along that axis for which the value of the quantity along the other axis is zero (0).
			</p>
			<nums>
			    <ul>
				Note the following;
				<li>If the intercept is required from the vertical axis (y-axis), the starting point of the vertical axis can be any value <b>BUT</b> the starting point for the
				    Horizontal axis must be at Zero.</li>
				<li>
				    If the intercept is required from the Horizontal axis, the starting point for the horizontal axis can be any value <b>BUT</b> the
				    starting point for the vertical axis must be Zero.
				</li>
				<li>
				    If the intercept is require from both of the axes, then both of the axes must start from Zero as their Origins.
				    This means that Two intercepts e.g C<sub>1</sub> and C<sub>2</sub> are to be determined.
				</li>
				<li>
				    When determining the intercept, clearly indicate the point where the intercept is. This is where the line of best fit meets the axis from which the intercept is
				    required.
				</li>
				<li>
				    The number of significant figures for the intercept must be the number of Significant figures of the leading value from that column on that axis. E.g,
				    if the first value in the column represented on the axis has 3 significant figures, then the intercept must also have 3 significant figures.
				</li>
				<li>
				    If only the slope is required, and there are no intercepts asked for, it may not be necessary for the axes to start from zero especially
				    where it is found inconvenient.
				</li>
			    </ul>
			</nums>
		    </div>
		    <hr/>
		    <h3 id="scale">4. Scale</h3>
		    <p>
			The scale chosen to be used must be;
		    </p>
		    <nums>
			<ul>
			    <li>Suitable i.e covers at least 75% of the graph paper on both axes.</li>
			    <li>Convenient i.e. should be easy to use without any auxiliary calculations.</li>
			    <li>Uniform on boh of the axes.</li>
			    <li>Easy to interpret and follow</li>
			    <li>able to accommodate all the values in the column of the Table of results</li>
			</ul>
		    </nums>
		    <p>
			Starting points of the of each scale must be indicated clearly and the points where the scale is indicated on the axes must be written clearly
			for easy interpretation. Avoid crossings and Untidy work on the Axes.
		    </p>
		    <hr/>
		    <h4 id="obtaining_convenient_scale">How to obtain a Convenient Scale.</h4>
		    <p>
			A convenient scale is one that can easily be used by the Person plotting to indicate the values and By the person marking to read the values.
			The following ways can guide you in determining the convenient scale to use for any type of Graph;
		    </p>
		    <nums>
			<ul>
			    <li>
				Note the Maximum and minimum values from the columns to be plotted against each other.
			    </li>
			    <li>
				Increase the maximum value by a small value and reduce the minimum by a small value. <br/>
				This helps in widening the range to be used while plotting on the graph and it helps you to use more space on the graph paper.
				<br/>
				<b>If an intercept was requested on any of the Axes, then decrease the minimum value for the other axis to an New minimum of Zero.</b>
			    </li>
			    <li>
				Determine the range between the New Maximum value and the New Minimum values for both of the axes.
			    </li>
			    <li>
				For the vertical scale, you are advised to use about 20cm, that is 100 small squares or division on the vertical axis. Therefore,
				divide the range for the vertical axis by 100<br/>
				For the Horizontal scale, you are advised to use  16 cm, that is about 80 small squares or divisions on the Horizontal axis. Therefore,
				divide the range for the Horizontal axis by 80.
			    </li>
			    <li>
				The figure values obtained in (4.) above represent what each small square represents on the Vertical and Horizontal axes respectively.<br/><br/>
				For convenience, we use scales involving digits <b> 1, 2, 4, 5, 8, 10,</b> their multiples or their sub-multiples such as
				<b>0.1, 0.2, 0.4, 0.5, 0.8, 0.10 or 10, 20, 30, 40, 50, 80, 100, etc...</b>
			    </li>
			    <li>
				If the figure value obtained in (4.) falls on one of the convenient scales like the ones in (5.), then use it.
			    </li>
			    <li>
				If the figure value obtained in (4.) does not fall on one of the convenient scales, then, take the nearest upper value from the set of the convenient scales. E.g.
				<br/><b>If the figure values obtained is 0.043, take 0.05, if the value obtained is 3.2, take 4, if the value obtained is 0.008356, take 0.01, etc...</b>
				The value chosen is what one(1) small square or division represent on the particular axis.
			    </li>
			    <li>
				Multiply the figure value chosen or obtained from (4.) by 10 to obtain what 2cm (10 small squares) will represent.
			    </li>
			    <li>
				Indicate the scale values on the axes starting from the <b>New Minimum</b> as the origin and adding the value obtained for 2cm (in (8.) above)
				using an interval of 2cm (10 small squares).
			    </li>
			    <li>
				If the scale used leave out some values, use another greater scale from the convenient scale values, e.g. if the scale of 0.01 fails,
				use 0.02, or 0.02 fails, use 0.04 or 0.04 fails, use 0.05, e.t.c ...
			    </li>
			    <h5>NOTE;</h5>
			    <p>
				It is important to note that the scale involving digits 4 and 8 are not so much convenient though sometimes we use them.<br/>
				In cases where the scale obtained involves 4 as the only significant figure e.g. in 0.04, replace 4 with 5 and use 0.05. This is much more convenient that 0.04.<br/><br/>
				in case where the scale obtained involves 8 as the only significant figure, use 10, e.g if the scale obtained is 0.008, use 0.010. This is more convenient than 0.008.
			    </p>
			</ul>
		    </nums>
