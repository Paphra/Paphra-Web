		<div class="">
		    <h3 id="units_of_the_final_answer">10. Units for the Final Answer.</h3>

		    <div>
			<p>
			    Before determining the units for the final Quantity, know the quantity you are supposed to determine
			    . This is usually stated in the Aim of the Experiment.
			    If the quantity you are determining is a known quantity clearly defined in the aim of the experiment
			    e.g. acceleration due to gravity, g, the mass M of a Metre rule, internal resistance of a dry cell, etc...
			    <b>Then the units must be the SI Unit of the quantity.</b>
			    If the quantity you are determining is an unknown constant (a constant with unknown unit), then
			    the unit for this constant must be derived from the given formula.
			    It is therefore important to read and understand the Aim of the experiment before starting to work with the apparatus. This
			    helps you to know the quantity you are supposed to determine.
			</p>

