		<h3 id="inttroduction">Introduction to Graph Works</h3>
		<p>
		    Graphs are normally used to analyze data obtained from an experiment. The values that you obtain from the process of carrying out
		    an experiment, are usually used in the plotting of a graph that would give the final answer to the in line with the aim of the Experiment.
		</p>
		<hr/>
		<h4>Things to Consider for any Question.</h4>
		<div>
		    <h5 id="title">1. Title</h5>
		    <p>
			A graph title describes which Quantity is to be plotted against which Quantity.
			It is obtained from the procedure that Instructs you to <b>Plot a graph of "something" against "something".</b>
			this procedure should be carefully considered because it points at which Quantities to focus on while plotting your graph for the given
			question.
			<br/>Note the Following for any title of any graph;
		    </p>
		    <nums>
			<ul>
			    <li>The graph must have a title clearly written at the top of the Graph Paper.</li>
			    <li>
				<p>
				    The title should be properly written in only one line e.g, <b>A graph of T<sup>2</sup> against x<sup>2</sup>.</b>.
				    <br/>This means that the values of T<sup>2</sup> are to be plotted along the vertical Axis and the Values of x<sup>2</sup>
				    along the Horizontal axis of the Graph.
				</p>
			    </li>
			    <li>The title of the graph must be written as; "<b>A graph of T<sup>2</sup> against x<sup>2</sup></b>", not in any other format.</li>
			    <li>Units of the physical quantities to be plotted against each other must not be included in the title of the graph.</li>
			    <li>
				<p>
				    The title must be written as it is given in the the procedure, i.e. the letters or symbols representing the physical quantities must be written
				    as given in the procedure. <b>Do not capitalize the symbols or Letters if they are given in the small letters and do not make them small letters if the procedure gives you
					capital Letters.</b> If you the opposite, this will mean that you are plotting a totally different graph from the one you are
				    given in the procedure. It is advisable to write your title in small letters keeping the cases for the Letters representing the physical quantities the way
				    they are given to you and only capitalizing the first letter <b>A</b>
				</p>
			    </li>
			</ul>
		    </nums>
		    <hr/>
		    <h3 id="axes">2. The Axes</h3>
		    <p>
			The axes for any graph must have the following qualities;
		    </p>
		    <div class="row">
			<div class="eight columns">
			    <nums>
				<ul>
				    <li>
					<p>
					    The axes must be drawn perpendicular to each other with arrows to show the direction in which the values are increasing. the type of
					    arrow used should be preferably <span class="fa fa-arrow-right"></span> for the horizontal axis and <span class="fa fa-arrow-up"></span>
					    for the vertical axis.
					</p>
				    </li>
				    <li>
					<p>
					    Each axis must be clearly and correctly labelled with the quantity and the appropriate unit where applicable. The unit for each quantity must be in brackets
					    and should be at the same level except for the degrees. E.g. m(kg). The degrees should be written slightly above the level of the quantity e.g. i(<sup>0</sup>).
					</p>
				    </li>
				    <li>
					The axes <b>must not</b> be drawn using broken lines.
				    </li>
				    <li>
					<p>
					    Each axis must be marked after every 10 small divisions (2cm intervals) starting from the origin.<br/>
					    it is important to note that the graph may not necessarily start from the origin (0,0).<br/>
					    When drawing the axes, select a suitable position on the graph paper and draw the axes so as to cover all the values (Positive and Negative values if present) in your
					    Table of results.
					</p>
				    </li>
				    <li>The starting points on both axes must be clearly indicated for proper interpretation of the graph.</li>
				    <li>
					<p>
					    Note that, for a graph to be valid, it must cover at least three quarters of the graph paper. The graph must not be too small as this would bring about congestion of
					    the plotted points. To make this possible, you are advised to leave about two centimeters on all the sides of the graph paper such that the axes are drawn away from the ends of
					    the graph paper but not so much in the center of the graph paper.
					</p>
				    </li>
				    <li>
					The axis takes the name of the quantity that it holds. E.g if the axis holds M(kg), then it is called the <b>M-axis</b>.
				    </li>
				</ul>
			    </nums>
