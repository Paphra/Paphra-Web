				    The best straight line in most elementary work is drawn according to one's judgment.
				    For straight line graphs, the line of best fit to the plotted data is that line which passes through
				    most of the plotted points leaving an equal or almost equal number of points on either side of the line if there is any scattered points.

				</p>
			    </div>
