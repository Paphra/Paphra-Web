		<h3 id="use_scale">5. How to use the Scales to plot points on the graph.</h3>
		<br/>
		<nums>
		    <ul>
			<li>
			    To plot a given point, subtract the minimum value on the axis (origin of the axis) from the value to be plotted</li>
			<li>
			    Then, divide the obtained differences for the quantities to be plotted by their respective scales (what each small square represents)
			    to obtain the number of small squares to be counted on
			    each axis starting from the minimum figure on the axis (Origin of the Axis).</li>
			<li>
			    Then, locate the position of the point by counting the small squares obtained on each axis.</li>
			<li>
			    If the axes start from Zero, just divide the values from the table of results by the what each small square represents on each axis, this will give the
			    the number of small squares to be counted from the origin.
			</li>
			<li>
			    <p>
				<b>Note:</b>
				The obtained value after dividing should be taken as it is, do not round off. This means that if the number of small squares obtained is let's say 2.67,
				you should count 2.67 small squares not 3 small squares.
				This is done for the concern of accuracy of the plotted points.
			    </p>
			</li>
		    </ul>
