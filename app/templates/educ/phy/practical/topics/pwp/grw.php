				    To determine the number of significant figures for the final value of the slope, we consider the number of significant
				    figures for the leading values in the columns for the Quantities plotted.
				    The slope should then be written to the least number of significant figures determined by comparing the significant figures
				    of the leading values from the table of results.
				    For instance, if the leading value in the column for the Y values has 3 s.f, and the leading value in the column for X
				    has 2 s.f, then, the final value for the slope should be written to 2 s.f. This is because, a slope is a ratio and ratios are divisions,
				    and the laws governing the significant figures for division, state that the final answer should always be written to the
