		<h3>Introduction to the Table of Results</h3>
		<p>
		    The Table of results is a very crucial item in the student's performance as this Table carries, in most cases, Half of the marks given
		    on any number. Sometimes, it is less than Half but it is a substantial amount of marks.<br/><br/>
		    It is very important as this is where you get the Values to be plotted in the graph.<br/>
		    The table should be a neatly drawn item on a paper in the booklet and it should be closed on all sides. This is done to inform the examiner
		    that no more data was missing in the table of results.

		</p>
		<h5>Note the Following:</h5>
		<div>
		    <nums>
			<ul>
			    The following should be done carefully and rightly for a table of results to be considered valid;
			    <li>The Items or Fields should be written once. Every item must have One column and the Items must be written at the top of the column.</li>
			    <li>The Units for each Item must be written in (brackets) and only once against their Items/Quantities.</li>
			    <li>Quantities with out units should only be written without any brackets following them.</li>
			    <li>Units for each particular quantity must be the standard units of measurement from a given Apparatus. <b>Unless</b> instructed to
				note the value in another unit.<br/>
				For Example;<br/>
				<div id="expl">
				    If a procedure is directing you to note the length L in metres, the Quantity must be recorded as <b>L (m)</b>.<br/>
				    But for a procedure that instructs you to just note the length L from the Metre rule, the Quantity should be written as <b>L (cm)</b>.<br/>
				    This is because, the standard unit of measurement from the metre rule is <b>cm</b>.<br/>
				    in the first case, the instructions are clear, record in metres, therefore, the values should be converted from the Standard unit of measurement to Metres which is the S.I Unit of Length.
				</div>
			    </li>
			    <li>Quantities involving Divisions and multiplications, the units should also be as a result of Division or Multiplication of the Respective units of the Quantities being divided or multiplied.<br/>For example;<br/>
				<div>
				    <p>
					If the procedure requires to obtain the value of <span>L</span>/<sub>d</sub> and all the values are lengths, then the resulting value has no units because the units for length will cancel.<br/>
					But if you are dividing units which are different, always use the <b>per</b> sign.<br/>For example;
				    </p>
				    <p>
					If a procedure requires you to obtain the value of <sup>L</sup>/<sub>M</sub> and L is in cm while M is in kg, the resulting unit is cmkg<sup>-1</sup><br/>
					This works even for squared values or values which have different powers. E.g, L(cm) divide by M<sup>2</sup>(kg<sup>2</sup>). The resulting Quantity is
					<b><sup>L</sup>/<sub>M</sub> (cmkg<sup>-2</sup>)</b>
				    </p>
				    <p>
					For values being multiplied, the same criteria is used. The units are a result of Multiplying the units of the Quantities being multiplied.
					<b>For example;</b> If the values have the same units, then the unit for the product is a square of their Units. And if the units are different,
					the resulting unit is just a combination of the units of the units for each particular quantity being multiplied.
				    </p>
				</div>
			    </li>
			    <li>
				<p>
				    Lastly, if their are two procedures requiring you to measure the lengths, L and d but one of the procedure instructs you
				    to measure in the S.I unit, then convert even the other unit to the S.I units for your
				    work to be consistent. This is helpful and important in case there is another procedure that instructs you to obtain the sum, difference , product
				    or division of those two or more quantities.
				</p>
			    </li>
			</ul>
		    </nums>
		</div>
	    </div>

