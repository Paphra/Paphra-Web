		    <h3 id="significant_figures">Significant Figures to Use in the Table of results.</h3>
		    <div>
			<p>
			    Use significant Figures for the Values to Be Manipulated.
			    For manipulated values like the ones obtained through Division, Multiplication, Subtraction
			    and Addition, the following rules should be followed;
			</p>

			<div class="w3-table-all">
			    <table>
				<thead>
				<th>Values</th>
				<th>How to Obtain Acceptable Value</th>
				</thead>
				<tr>
				    <td>Multiplication</td>
				    <td>
					<p>
					    Your answer must be to the least number of Significant Figures as possessed by any Quantity in the expression
					    <br/>For example; If an expression contains one Item with 2 Significant figures and another with 3 Significant figures, then the
					    final answer should be to two (2) significant figures.
					</p>
				    </td>
				</tr>
				<tr>
				    <td>Division</td>
				    <td>
					<p>
					    Just like Multiplication, your answer must be to the least number of Significant Figures as possessed by any Quantity in the expression
					    <br/>For example; If an expression contains one Item with 2 Significant figures and another with 3 Significant figures, then the
					    final answer should be to two (2) significant figures.
					</p>
				    </td>
				</tr>
				<tr>
				    <td>Subtraction</td>
				    <td>
					<p>
					    Here, the number of decimal places is considered.
					    The least number of decimal places possessed by any quantity in the expression is taken for the final answer.<br/>
					    For example; If an expression contains one Item with 2 Decimal Places and another with 3 Decimal Places, then the
					    final answer should be to two (2) Decimal Places.
					</p>
				    </td>
				</tr>
				<tr>
				    <td>Addition</td>
				    <td>
					<p>
					    Just like addition, here, the number of decimal places is considered.
					    The least number of decimal places possessed by any quantity in the expression is taken for the final answer.<br/>
					    For example; If an expression contains one Item with 2 Decimal Places and another with 3 Decimal Places, then the
					    final answer should be to two (2) Decimal Places.
					</p>
				    </td>
				</tr>
				<tfoot>
				<td colspan="2">
				    <h5>Note that;</h5>
				<nums>
				    <ul id="listNum">
					<li>The Significant figures for any column are considered for the First Value in the Table of results in the First Row for all the Columns</li>
					<li>For all the remaining rows, the number of decimal places in the first Value in the Column are considered for all the rows.</li>
					<li>This is done to ensure uniformity in the columns and where by all values in the same column <b>Must</b> have the same number of Decimal Places.</li>
					<li>For constants like 2, 400, 3.0, given to you or defined as Constants like g = 10 ms<sup>-2</sup> or the Value of pi, their significant Figures are not considered.</li>
				    </ul>
				</nums>
				</td>
