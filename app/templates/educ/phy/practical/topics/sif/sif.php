				    <hr/>
				    <div class="" id="hide">
					<p>
					    The Micrometer screw gauge is used for measuring smaller lengths of at-most
					    1cm or 10mm such as diameter of a wire. It has two scales, i.e; the man scale and
					    the thimble scale or circular scale. <br/>
					    The main scale is graduated in millimeters whereas the circular scale has
					    either 50 or 100 divisions.
					</p>
					<image src="../img/labelled_micrometer_screw_gauge.jpg" title="Micrometer Screw Gauge"
					       height="" width="" alt="Micrometer Screw Gauge"/> <br/>
					<b><i>Fig 1.1.1 Micrometer Screw Gauge</i></b>
					<p>
					    The micrometer screw gauge has an accuracy of 2 decimal places (0.01mm).
					    Before using a micrometer screw gauge, its scale must be studied to determine
					    the zero error. The zero error must be subtracted from the subsequent measurement.
					</p>
