		<li><h4><a href="#rules_used" onclick="show('rules_used')">Convention Used in Writing Units and Symbols of Quantities</a></h4></li>
		<div style="display: none;" class="row" id="rules_used">
		    <hr/>
		    <div class="" id="hide">
			<p>
			    The following rules are to be followed when writing units and symbols of quantities.
			</p>
			<nums>
			    <ul>
				<li>
				    Units must be stated in full or using their right symbols. Abbreviations of
				    units should not be used. E.g; the unit for time is seconds or s and not sec. Time of
				    20 seconds is recorded as 20 seconds or 20s not 20sec, mass of 0.200 in kilograms should be
				    recorded as 0.200 kilograms or 0.200kg and not as 0.200kgms, e.t.c.
				</li>
				<li>
				    A Unit named after a scientist is to be written with a capital letter <b>for-example</b> newton(N), joule(J), ampere(A),
				    kelvin(K), volt(V), among others. If such a unit is to be written in full, small letters are to be used, <b>for-example</b>, amperes, newtons,
				    joules, volts, among others; Except for Celsius.
				</li>
				<li>
				    Units which are not named after scientist should be written in small letters only, <b>for-example</b>, kg, km, s, m, among others.
				</li>
				<li>
				    Symbols of units remain unaltered in plural form but when written in words, plurals can be used <b>for-example</b>; symbol for
				    20 kilograms is 20kg and not 20kgs, symbol for 10 centimeters is 10cm and not 10cms, among others.
				</li>
				<li>
				    If a quantity is written in the table, the unit must be written once at the top of the column inside the brackets ()
				    and not after a forward slash /, <b>for-example</b>; mass m in kilograms is written as m(kg) not m/kg, length l in meters is written
				    as l(m) not l/m, Period T in seconds is written as T(s) and not T/s, among others.
				</li>
				<li>
				    The symbols for units are not to be followed by a full stop. For-example, the symbol for metres is m (not m.), the
				    symbol for seconds is s (not s.), symbol for kilogram is kg (not kg.), among others.
				</li>
				<li>
				    When recording experimental values (directly measured values), the unit attached must be the unit of the instrument
				    used unless when required to record otherwise, <b>for-example</b>; if the values of a given quantity are required in SI units, then
				    they must be recorded in SI units.
				</li>
				<li>Sines, cosines, tangents of angles and logarithms do not have units.</li>
				<li>
				    When recording values in the table, the units used must be those specified in the procedure, or if not specified, the
				    units of the instrument used in the measurement of the quantity.
				</li>
				<li>
				    Symbols used must be written clearly and correctly as required. If the symbol used is not given or defined in the instructions
				    it must be clearly defined. Do not change the symbols and the case of the letters used in the procedure <b>for-example</b>; m to M, x to X,
				    l to L and vice versa.
				</li>
				<li>
				    Units of derived quantities should be written using scientific notation <b>for-example</b>; ms<sup>-2</sup> not m/s<sup>-2</sup>,
				    kgm<sup>-3</sup> not kg/m<sup>-3</sup>
				</li>
			    </ul>
